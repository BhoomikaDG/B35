package org.tnsindia.polymorphismdemo;
//method overloading with different data type
public class methodverloading {
	public void multilication(int x,int y)
	{
		System.out.println("The result is:"+(x*y));
		
	}
	
	public void multilication(double x,int y)
	{
		System.out.println("The result is:"+(x*y));
		
	}
	
}



